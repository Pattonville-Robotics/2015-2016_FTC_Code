package com.electronauts.virtualrobot;

/**
 * The MotorData Enum specifies what location the motor is in the robot, and allows the motor to be located in the robot's motor array.
 */
public enum MotorData
{
	/** A left motor. */
	MOTOR_LEFT,

	/** A right motor. */
	MOTOR_RIGHT,

	/** A front left motor. */
	MOTOR_FRONT_LEFT,

	/** A front right motor. */
	MOTOR_FRONT_RIGHT,

	/** A back left motor. */
	MOTOR_BACK_LEFT,

	/** A back right motor. */
	MOTOR_BACK_RIGHT
}
